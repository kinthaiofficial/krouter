package config

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

const (
	markerBegin = "# >>> krouter shell integration >>>"
	markerEnd   = "# <<< krouter shell integration <<<"
)

// ShellInitOutput returns the shell export statements for the given shell type.
// shell: "fish" emits set -gx; anything else emits export.
func ShellInitOutput(shell string) string {
	if shell == "fish" {
		return "set -gx ANTHROPIC_BASE_URL \"http://localhost:8402\"\n" +
			"set -gx OPENAI_BASE_URL \"http://localhost:8402/v1\"\n"
	}
	return "export ANTHROPIC_BASE_URL=\"http://localhost:8402\"\n" +
		"export OPENAI_BASE_URL=\"http://localhost:8402/v1\"\n"
}

// DetectShellRC returns the shell rc file path based on $SHELL.
// Defaults to ~/.bashrc if $SHELL is not set or unrecognized.
func DetectShellRC() string {
	home, _ := os.UserHomeDir()
	shell := filepath.Base(os.Getenv("SHELL"))
	switch shell {
	case "zsh":
		return filepath.Join(home, ".zshrc")
	case "bash":
		return filepath.Join(home, ".bash_profile")
	case "fish":
		return filepath.Join(home, ".config", "fish", "config.fish")
	default:
		return filepath.Join(home, ".bashrc")
	}
}

// ConnectClaudeCode appends the kinthai shell integration marker block to shellRCPath.
// Idempotent: skips if the marker is already present.
func ConnectClaudeCode(shellRCPath string) error {
	content, err := readOrEmpty(shellRCPath)
	if err != nil {
		return fmt.Errorf("claude-code: read rc: %w", err)
	}

	if strings.Contains(content, markerBegin) {
		return nil // already installed
	}

	block := "\n" + markerBegin + "\n" +
		"# Auto-generated by krouter install wizard. Do not edit manually.\n" +
		"eval \"$(krouter shell-init)\"\n" +
		markerEnd + "\n"

	if err := os.MkdirAll(filepath.Dir(shellRCPath), 0755); err != nil {
		return fmt.Errorf("claude-code: mkdir: %w", err)
	}

	f, err := os.OpenFile(shellRCPath, os.O_APPEND|os.O_CREATE|os.O_WRONLY, 0644)
	if err != nil {
		return fmt.Errorf("claude-code: open rc: %w", err)
	}
	defer func() { _ = f.Close() }()

	_, err = f.WriteString(block)
	return err
}

// DisconnectClaudeCode removes the kinthai marker block from shellRCPath.
func DisconnectClaudeCode(shellRCPath string) error {
	content, err := readOrEmpty(shellRCPath)
	if err != nil {
		return fmt.Errorf("claude-code: read rc: %w", err)
	}

	if !strings.Contains(content, markerBegin) {
		return nil // nothing to remove
	}

	lines := strings.Split(content, "\n")
	var out []string
	inBlock := false
	for _, line := range lines {
		if strings.TrimSpace(line) == markerBegin {
			inBlock = true
			continue
		}
		if strings.TrimSpace(line) == markerEnd {
			inBlock = false
			continue
		}
		if !inBlock {
			out = append(out, line)
		}
	}

	result := strings.Join(out, "\n")
	// Remove extra blank lines that may have been left.
	result = strings.TrimRight(result, "\n") + "\n"

	return os.WriteFile(shellRCPath, []byte(result), 0644)
}

func readOrEmpty(path string) (string, error) {
	data, err := os.ReadFile(path)
	if os.IsNotExist(err) {
		return "", nil
	}
	return string(data), err
}
